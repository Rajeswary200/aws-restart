print("Python has three numeric types: int, float, and complex")
print("\nInteger data type \n-----------------")
myValue=1
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))
print("\nFloat Data type \n-------------------")
myValue=3.14
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))
